	<h1>CIÊNCIA DE DADOS: O QUE É, CONCEITO E DEFINIÇÃO</h1>

	<img src="imagens/datascience.png">

	<p class="Texto">Por mais que você ainda não tenha se atentado a esse fato, grande partes dos negócios já faz parte do mercado de dados e informações.

A expectativa é que em 2017 sejamos cerca de 5 bilhões de mobile phones conectados, que serão responsáveis por produzir um fluxo gigantesco e constante de informação digital.<br>

A estimativa afirma que cerca de 90% de todos os dados armazenados em todo o mundo foram produzidos somente nos dois últimos anos e seus rastros continuam se multiplicando a cada ano que passa.
Quando analisamos essas informações parece tudo muito assustador e impossível de ser controlado, certo? – Errado! – A ciência de dados está cada vez mais consistente no mercado para que possamos acompanhar esse crescimento sem tropeços no futuro. E sabe qual o resultado dessa evolução toda de informações? – O Big  Bang do Dataverse. – Hpa afirmações que essa será a próxima onda.<br>

Portanto, inevitavelmente grande parte das empresas fazem parte desse ecossistema de dados, e sem um acompanhamento e analises cuidadosas em um curto prazo será quase impossível tomar decisões inteligentes a favor de bons resultados. É importante destacar que os dados permitem que as empresas e organizações entendam melhor seus clientes e consumidores e possam aprimorar cada vez mais os seus processos.<br>

Diante de todos esses fatos, a Data Science ou Ciência de Dados tem sido muito mais relevante em noticias sobre indústria e negócios, e não é pra menos, pois trata-se de uma disciplina acadêmica e profissão totalmente em alta no mercado.<br>

Para não haver confusão, é importante destacar que a ciência de dados é diferente das analises estatísticas e também da ciência da computação com relação ao seu método aplicado aos dados coletados, usando princípios científicos.<br>

A principal razão disso é o chamado Big Data, que é responsável por demandar o uso de diferentes tecnologias com relação à analise estatística. Em outras palavras, profissionais de estatísticas existentes a anos no mercado não conseguiriam fazer uma analise profunda dos dados em massa quase que em tempo real, que é o que ocorre nos dias de hoje nas grandes empresas.<br>

Nesse caso, a ciência de dados está buscando descobrir conhecimento demandável a partir de uma quantidade grande e pesada de dados que podem ser usadas para tomar decisões e fazer previsões, e não simplesmente a interpretação de números. É por esse motivo que o mercado está cada vez mais em busca de cientistas de dados, que possuem requisitos como o conhecimento em engenharia e o Ph.D também.

